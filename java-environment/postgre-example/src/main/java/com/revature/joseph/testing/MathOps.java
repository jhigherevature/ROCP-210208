package com.revature.joseph.testing;

/*
 * This Math Operations class will be used to showcase some basics 
 * of Unit Testing.
 */
public class MathOps {
	public int add(int i, int j) {
		return i + j;
	}
	
	public int subtract (int a, int b) {
		return a - b;
	}
	
	public int divide (int x, int y) {
		return x / y;
	}
}
