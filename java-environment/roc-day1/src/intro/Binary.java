package intro;

public class Binary {
	/*
	 * Binary is a way of representing information
	 * using 0's and 1's
	 * 
	 * Humans use a base-10 counting system. We have 
	 * ten digits to represent different combinations
	 * of numerical values:
	 * 1, 2, 3, 4, 5, 6, 7, 8, 9, 0
	 * 
	 * Binary is a base-2 counting system. This means
	 * we have two digits to represent any combination
	 * of numerical values.
	 * 
	 * In base:
	 * 0
	 * 1
	 * 2
	 * 3
	 * ...
	 * 9
	 * 10
	 * 
	 * In binary:
	 * 0000 - 0
	 * 0001 - 1
	 * 0010 - 2
	 * 0011 - 3
	 * 0100 - 4
	 * 
	 * 1111 - 15
	 * 
	 * 
	 * When we move between datatype, we truncate
	 * the binary values for the data:
	 * ex 8 bits to 4 bits:
	 * 0101 1101 - 8 bits holds all the data
	 * 
	 * 1101 - 4 bits truncates the left-most bits
	 */
}
